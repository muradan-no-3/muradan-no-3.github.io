function brightness(hex) {
  if (!hex) {
    return 0;
  }
  const rgb = hex.substring(1);
  const r = parseInt(rgb.substring(0, 2), 16);
  const g = parseInt(rgb.substring(2, 4), 16);
  const b = parseInt(rgb.substring(4, 6), 16);
  const result = (0.299 * r + 0.587 * g + 0.114 * b) / 255;

  return result;
}
